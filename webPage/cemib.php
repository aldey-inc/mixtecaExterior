Apegados a  las normas oficiales mexicanas y a nuestro manual de organización que se rige bajo los
siguientes principios: 

Misión

Brindar permanentemente atención medica de excelencia a través de los siguientes principios:
	Ofrecer infraestructura y equipo que respondan plenamente a las necesidades y demandas de la población.
	Tener personal médico y paramédico altamente capacitado, con talento y experiencia.
	Brindar al paciente un trato honesto y cálido.

Visión

Conformar el sistema de salud privado más completo en la ciudad de Huajuapan de león, que se distinga
por proporcionar un servicio de calidad a sus pacientes, con oportunidad de desarrollo profesional y
personal a sus empleados.

Objetivo

Ser una clínica que brinda atención medica de calidad a través de infraestructura y equipo humano
profesional y altamente capacitado para otorgar al paciente interno y externo un trato honesto y cálido
que responda a las necesidades y demandas de la sociedad.

Filosofía

Contar con un equipo médico de especialistas con una sólida formación profesional y alto espíritu de
servicio para brinda asistencia médica a la altura de los mejores hospitales.


Valores

Ética profesional
    Apegarnos en todo momento a la moral y a la ética, manifestando respeto por la Condición de los pacientes
    y realizar únicamente acciones que les beneficien.  
 
Humanismo
	Siempre se procura el mayor bienestar de cada  uno de los pacientes, brindando un servicio con respeto,
	honestidad y  amabilidad. 

Honestidad
	Hablar siempre con la verdad, respondiendo a la confianza  y la fe que cada uno de los pacientes ha depositado
	en nosotros. 

Responsabilidad
	Actuar con seguridad ante  las necesidades de todos y cada uno de los pacientes, cumpliendo así nuestra misión
	y objetivo. 

Competitividad
	Cumplir con los compromisos y retos del trabajo diario, con gusto y optimismo. Empleando de manera adecuada todos
	los valores y recursos de la empresa, logrado una plena satisfacción de los pacientes. 

Calidad de Servicio
	Todas las actividades llevadas a cabo por el hospital, son tratadas como procesos, y como tal son planificados, medidos,
	controlados, analizados y mejorados de forma continua.
